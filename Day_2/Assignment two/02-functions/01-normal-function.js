// Write a normal function to add two numbers

function addNumbers(num1, num2) {
  return num1 + num2;
}

// Test the function
console.log("Normal Function - Add Two Numbers");
console.log("addNumbers(5, 3):", addNumbers(5, 3));        // 8
console.log("addNumbers(10, 20):", addNumbers(10, 20));    // 30
console.log("addNumbers(-5, 15):", addNumbers(-5, 15));    // 10
console.log("addNumbers(0, 0):", addNumbers(0, 0));        // 0
